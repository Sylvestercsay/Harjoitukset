Ensimmäinen = int(input("Syötä ensimmäinen kokonaisluku: "))
Toinen = int(input("Syötä toinen kokonaisluku: "))
Kolmas = int(input("Syötä kolmas kokonaisluku: "))

Kokonais = Ensimmäinen + Toinen + Kolmas
Tuote = Ensimmäinen * Toinen * Kolmas
Keskiarvo = Kokonais / 3

print (f"Summa : {Kokonais}")
print (f"Tuote: {Tuote}")
print (f"Keskiarvo: {Keskiarvo}")